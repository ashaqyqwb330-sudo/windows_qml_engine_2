tasks.register<Copy>("copySourcesToAssets") {
  group = "build"
  description = "Copies application sources to assets/source_tree for self-exporting features."
  val destDir = file("${projectDir}/src/main/assets/source_tree")

  // Ensure output dir is cleaned before copying
  doFirst {
    if (destDir.exists()) {
      destDir.deleteRecursively()
    }
    destDir.mkdirs()
  }

  into(destDir)

  // 1. App Java/Kotlin files
  from("${projectDir}/src/main/java") {
    include("**/*.kt")
    into("app/src/main/java")
  }

  // 2. Android Manifest
  from("${projectDir}/src/main") {
    include("AndroidManifest.xml")
    into("app/src/main")
  }

  // 3. Android Resources
  from("${projectDir}/src/main/res") {
    include("**/*")
    into("app/src/main/res")
  }

  // 4. App-level Gradle config
  from(projectDir) {
    include("build.gradle.kts")
    into("app")
  }

  // 5. Root Project configurations
  from(rootDir) {
    include("build.gradle.kts")
    include("settings.gradle.kts")
    include("gradle.properties")
    include("gradle/wrapper/gradle-wrapper.properties")
    include("gradle/libs.versions.toml")
    into(".")
  }
}

tasks.named("preBuild") {
  dependsOn("copySourcesToAssets")
}

